void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	HAL_ADC_Start_DMA(&hadc1,&buffer,1);
	if(headerSet == 0x00){
		  f_mount(&fs, "", 0);
		  f_open(&fil, "output.wav", FA_OPEN_ALWAYS | FA_READ | FA_WRITE);
		  f_write(&fil,header,44,&bw);
		  headerSet = 0x01;
	}
	if(laco == 0){
		HAL_GPIO_WritePin(GPIOC,GPIO_PIN_13,GPIO_PIN_RESET);
	}
	if(laco < samples){

		HAL_ADC_Start_DMA(&hadc1,&buffer,1);
		if(adcValue >= 0x061A && adcValue <= 0x088b){
			buff[count] = (0x07);
		    buff[count+1] = (0x53);
		}else{
			buff[count] = (adcValue >> 8);
		    buff[count+1] = (adcValue);
		}
		count++;
		count++;
		if(count >= 999){
			f_write(&fil,buff,1000,&bw);
			count = 0;
		}else if(laco == samples-1){
			f_write(&fil,buff,count,&bw);
			count = 0;
		}
		laco++;
	}else{
		if(closed == 0x00){
			f_close(&fil);
			closed = 0x01;
			HAL_GPIO_WritePin(GPIOC,GPIO_PIN_13,GPIO_PIN_SET);
		}
	}
}
